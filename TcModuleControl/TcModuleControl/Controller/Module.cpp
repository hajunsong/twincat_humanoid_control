///////////////////////////////////////////////////////////////////////////////
HRESULT CModule::AddModuleToCaller()
{
	m_Trace.Log(tlVerbose, FENTERA_STR());

	HRESULT hr = S_OK;
	if ( m_spCyclicCaller.HasOID() )
	{
		if ( SUCCEEDED_DBG(hr = m_spSrv->TcQuerySmartObjectInterface(m_spCyclicCaller)) )
		{
			if ( FAILED(hr = m_spCyclicCaller->AddModule(m_spCyclicCaller, THIS_CAST(ITcCyclic))) )
			{
				m_spCyclicCaller = NULL;
			}
		}
	}
	else
	{
		hr = ADS_E_INVALIDOBJID; 
		SUCCEEDED_DBGT(hr, "Invalid OID specified for caller task");
	}
	m_Trace.Log(tlVerbose, FLEAVEA_STRV("hr=0x%08x", hr));
	return hr;
}

///////////////////////////////////////////////////////////////////////////////
VOID CModule::RemoveModuleFromCaller()
{
	m_Trace.Log(tlVerbose, FENTERA_STR());

	if ( m_spCyclicCaller )
	{
		m_spCyclicCaller->RemoveModule(m_spCyclicCaller);
	}
	m_spCyclicCaller	= NULL;
	m_Trace.Log(tlVerbose, FLEAVEA_STR());
}

///////////////////////////////////////////////////////////////////////////////
// Per-cycle dispatch for Main::PathMotion on the single motor. Trajectory
// primitives come from humanoid::motion::* (header-only).
//
// This module's PathParameter has no nAccTime and no nProfileMode field, so the
// trapezoidal acceleration time uses kDefaultAccelRatio (clamped to
// [0, tf*0.5]) and reciprocate / stepping-offset always use the quintic blend.
void CModule::ApplyPathMotion()
{
	const USHORT subRaw = m_ClientToServer.SubCmd;
	const auto sub = static_cast<ClientCommand::PathSub>(subRaw);

	const bool firstTrapezoidal =
		(sub == ClientCommand::PathSub::TrapezoidalProfile)
		&& (m_prevPathMotionSub != static_cast<USHORT>(ClientCommand::PathSub::TrapezoidalProfile));
	const bool firstQuintic =
		(sub == ClientCommand::PathSub::QuinticPolynomial)
		&& (m_prevPathMotionSub != static_cast<USHORT>(ClientCommand::PathSub::QuinticPolynomial));
	const bool firstReciprocate =
		(sub == ClientCommand::PathSub::Reciprocate)
		&& (m_prevPathMotionSub != static_cast<USHORT>(ClientCommand::PathSub::Reciprocate));
	const bool firstSteppingOffset =
		(sub == ClientCommand::PathSub::SteppingOffsetMove)
		&& (m_prevPathMotionSub != static_cast<USHORT>(ClientCommand::PathSub::SteppingOffsetMove));

	const PathParameter& pp = m_ClientToServer.PathCmd;
	const bool updateRise = pp.nUpdate && !m_prevPathNUpdate;

	// === Setup phase (first-command or nUpdate rising edge) ===
	if (sub == ClientCommand::PathSub::TrapezoidalProfile && (firstTrapezoidal || updateRise))
	{
		double tf = pp.nTotalTime;
		double h = pp.nStepSize;
		int len = humanoid::motion::ComputeStepCount(tf, h);
		if (len <= 0)
		{
			m_trajValid = false;
		}
		else
		{
			double ta = tf * kDefaultAccelRatio;
			if (ta < 0.0)
				ta = 0.0;
			if (ta > tf * 0.5)
				ta = tf * 0.5;
			m_trajX0 = static_cast<double>(m_MotorStArea.Data.nActualPosition);
			m_trajXf = static_cast<double>(pp.nSetPoint);
			m_trajTf = tf;
			m_trajTa = ta;
			m_trajH = h;
			m_trajLength = len;
			m_trajStep = 0;
			m_trajIsTrapezoid = true;
			m_trajValid = true;
			m_trajPaused = false;
		}
	}
	else if (sub == ClientCommand::PathSub::QuinticPolynomial && (firstQuintic || updateRise))
	{
		double tf = pp.nTotalTime;
		double h = pp.nStepSize;
		int len = humanoid::motion::ComputeStepCount(tf, h);
		if (len <= 0)
		{
			m_trajValid = false;
		}
		else
		{
			m_trajX0 = static_cast<double>(m_MotorStArea.Data.nActualPosition);
			m_trajXf = static_cast<double>(pp.nSetPoint);
			m_trajTf = tf;
			m_trajTa = 0.0;
			m_trajH = h;
			m_trajLength = len;
			m_trajStep = 0;
			m_trajIsTrapezoid = false;
			m_trajValid = true;
			m_trajPaused = false;
		}
	}
	else if (sub == ClientCommand::PathSub::Reciprocate && (firstReciprocate || updateRise))
	{
		const LONG a = m_MotorStArea.Data.nActualPosition;
		const LONG b = pp.nSetPoint;
		double tf = pp.nTotalTime;
		double h = pp.nStepSize;
		int len = humanoid::motion::ComputeStepCount(tf, h);
		if (a == b || len <= 0)
		{
			m_recipValid = false;
		}
		else
		{
			m_recipPosStart = a;
			m_recipPosTarget = b;
			m_recipLegLength = len;
			m_recipTf = tf;
			m_recipH = h;
			m_recipTa = 0.0;
			m_recipUseTrapezoid = false;
			m_recipForward = true;
			m_recipLegStep = 0;
			m_recipValid = true;
		}
	}
	else if (sub == ClientCommand::PathSub::SteppingOffsetMove && (firstSteppingOffset || updateRise))
	{
		const LONG ap = m_MotorStArea.Data.nActualPosition;
		const LONG b = pp.nSetPoint;
		const LONG dlt = b - ap;
		double tf = pp.nTotalTime;
		double h = pp.nStepSize;
		int len = humanoid::motion::ComputeStepCount(tf, h);
		if (dlt == 0 || len <= 0)
		{
			m_stepOffValid = false;
		}
		else
		{
			m_stepOffDelta = dlt;
			m_stepOffSegX0 = static_cast<double>(ap);
			m_stepOffSegXf = static_cast<double>(b);
			m_stepOffLegLen = len;
			m_stepOffTf = tf;
			m_stepOffH = h;
			m_stepOffTa = 0.0;
			m_stepOffUseTrap = false;
			m_stepOffLegStep = 0;
			m_stepOffValid = true;
		}
	}

	m_prevPathNUpdate = pp.nUpdate;

	// === Playback phase (per-sub opcode) ===
	switch (sub)
	{
	case ClientCommand::PathSub::Reciprocate:
		if (m_recipValid)
		{
			if (m_recipLegStep >= m_recipLegLength)
			{
				m_recipForward = !m_recipForward;
				m_recipLegStep = 0;
			}

			const double x0 = m_recipForward ? static_cast<double>(m_recipPosStart) : static_cast<double>(m_recipPosTarget);
			const double xf = m_recipForward ? static_cast<double>(m_recipPosTarget) : static_cast<double>(m_recipPosStart);
			const int s = m_recipLegStep;
			double t = static_cast<double>(s) * m_recipH;
			if (t > m_recipTf)
				t = m_recipTf;
			double posd;
			if (m_recipUseTrapezoid)
				posd = humanoid::motion::Trapezoidal5thAtTime(x0, xf, m_recipTf, m_recipTa, t);
			else
				posd = humanoid::motion::Single5thPosition(x0, xf, m_recipTf, t);

			const LONG p = humanoid::motion::RoundToLong(posd);
			m_MotorCmdArea.Data.nTargetPosition = p;
			m_trajLastTargetPos = p;
			m_recipLegStep = s + 1;
		}
		break;
	case ClientCommand::PathSub::SteppingOffsetMove:
		if (m_stepOffValid)
		{
			int s = m_stepOffLegStep;
			while (s >= m_stepOffLegLen && m_stepOffLegLen > 0)
			{
				m_stepOffSegX0 = m_stepOffSegXf;
				m_stepOffSegXf += static_cast<double>(m_stepOffDelta);
				s = 0;
			}

			double t = static_cast<double>(s) * m_stepOffH;
			if (t > m_stepOffTf)
				t = m_stepOffTf;
			double posd;
			if (m_stepOffUseTrap)
				posd = humanoid::motion::Trapezoidal5thAtTime(m_stepOffSegX0, m_stepOffSegXf, m_stepOffTf, m_stepOffTa, t);
			else
				posd = humanoid::motion::Single5thPosition(m_stepOffSegX0, m_stepOffSegXf, m_stepOffTf, t);

			const LONG p = humanoid::motion::RoundToLong(posd);
			m_MotorCmdArea.Data.nTargetPosition = p;
			m_trajLastTargetPos = p;
			m_stepOffLegStep = s + 1;
		}
		break;
	case ClientCommand::PathSub::Pause:
		m_trajPaused = true;
		break;
	case ClientCommand::PathSub::Resume:
		m_trajPaused = false;
		break;
	case ClientCommand::PathSub::PlayPath:
		if (m_trajValid)
		{
			if (m_trajPaused)
			{
				m_MotorCmdArea.Data.nTargetPosition = m_trajLastTargetPos;
				break;
			}

			int s = m_trajStep;
			if (s >= m_trajLength)
			{
				LONG xf = humanoid::motion::RoundToLong(m_trajXf);
				m_MotorCmdArea.Data.nTargetPosition = xf;
				m_trajLastTargetPos = xf;
				break;
			}

			double t = static_cast<double>(s) * m_trajH;
			if (t > m_trajTf)
				t = m_trajTf;
			double posd;
			if (m_trajIsTrapezoid)
				posd = humanoid::motion::Trapezoidal5thAtTime(m_trajX0, m_trajXf, m_trajTf, m_trajTa, t);
			else
				posd = humanoid::motion::Single5thPosition(m_trajX0, m_trajXf, m_trajTf, t);

			LONG p = humanoid::motion::RoundToLong(posd);
			m_MotorCmdArea.Data.nTargetPosition = p;
			m_trajLastTargetPos = p;
			m_trajStep = s + 1;
		}
		break;
	case ClientCommand::PathSub::StopHoldAndResetPath:
	{
		m_stepOffValid = false;
		m_stepOffLegStep = 0;
		m_stepOffLegLen = 0;
		m_stepOffDelta = 0;
		m_recipValid = false;
		m_recipLegStep = 0;
		m_recipLegLength = 0;
		m_trajValid = false;
		m_trajPaused = false;
		m_trajStep = 0;
		m_trajLength = 0;
		m_trajX0 = 0.0;
		m_trajXf = 0.0;
		m_trajTf = 0.0;
		m_trajTa = 0.0;
		m_trajH = 0.0;
		m_trajIsTrapezoid = false;

		const LONG ap = m_MotorStArea.Data.nActualPosition;
		m_MotorCmdArea.Data.nTargetPosition = ap;
		m_trajLastTargetPos = ap;
		// CiA 402: hold at the current position (switch-on).
		m_MotorCmdArea.Data.nControlWord = humanoid::cia402::kControlSwitchOn;
		break;
	}
	default:
		break;
	}

	m_prevPathMotionSub = subRaw;
}

///////////////////////////////////////////////////////////////////////////////
// Dispatches ModuleClientToServer.MainCmd / .SubCmd onto the single motor
// command area. Forwards Main::PathMotion to ApplyPathMotion(); handles
// ControlDrive (CiA 402 enable / quit / fault-reset stepping), SetOperationMode
// (CSP / CSV / CST) and JogModeMove (CSV velocity jog) inline. CiA 402 numeric
// constants come from humanoid::cia402::*.
void CModule::ApplyMainCmdMotorOutputs()
{
	const auto mainCmd = static_cast<ClientCommand::Main>(m_ClientToServer.MainCmd);
	const USHORT subRaw = m_ClientToServer.SubCmd;

	MotorCmd& cmd = m_MotorCmdArea.Data;
	const MotorSt& st = m_MotorStArea.Data;

	// Re-assert the last commanded operation mode every cycle, before
	// dispatching the current command, so the mode (MotorCmd.nModeOfOperation,
	// CoE 0x6060) stays latched across MainCmds: after SetOperationMode(CSV) the
	// mode survives a following ControlDrive (Enable) instead of reverting to
	// the drive default. -1 means no mode has been commanded yet.
	if (m_commandedMode >= 0)
		cmd.nModeOfOperation = static_cast<char>(m_commandedMode);

	if (mainCmd == ClientCommand::Main::PathMotion)
	{
		ApplyPathMotion();
		return;
	}

	// Any non-PathMotion command resets the "previous path sub" tracker so the
	// next entry into PathMotion is treated as a fresh first command.
	m_prevPathMotionSub = 0;

	if (mainCmd == ClientCommand::Main::SetOperationMode)
	{
		const auto sub = static_cast<ClientCommand::ModeSub>(subRaw);
		SHORT mode;
		switch (sub)
		{
		case ClientCommand::ModeSub::Csp:
			mode = humanoid::cia402::kModeCsp;
			break;
		case ClientCommand::ModeSub::Csv:
			mode = humanoid::cia402::kModeCsv;
			break;
		case ClientCommand::ModeSub::Cst:
			mode = humanoid::cia402::kModeCst;
			break;
		default:
			return;
		}

		// Latch the requested mode (re-asserted every cycle, see above) and
		// apply it immediately this cycle too.
		m_commandedMode = mode;
		cmd.nModeOfOperation = static_cast<char>(mode);
		return;
	}

	if (mainCmd == ClientCommand::Main::JogModeMove)
	{
		// Jog runs in CSV (cyclic synchronous velocity): while the command is
		// held, TargetVelocity is driven to +/- JogCmd.nJogTick. Ignored unless
		// the drive reports CSV mode of operation. Stop sets velocity to 0.
		const LONG jogVelocity = static_cast<LONG>(m_ClientToServer.JogCmd.nJogTick);
		const auto sub = static_cast<ClientCommand::JogSub>(subRaw);

		if (st.nModeOfOperationDisp != humanoid::cia402::kModeCsv)
			return;

		switch (sub)
		{
		case ClientCommand::JogSub::Positive:
			cmd.nTargetVelocity = (jogVelocity > 0) ? jogVelocity : 0;
			break;
		case ClientCommand::JogSub::Negative:
			cmd.nTargetVelocity = (jogVelocity > 0) ? -jogVelocity : 0;
			break;
		case ClientCommand::JogSub::Stop:
			cmd.nTargetVelocity = 0;
			break;
		default:
			break;
		}

		return;
	}

	if (mainCmd != ClientCommand::Main::ControlDrive)
		return;

	const auto sub = static_cast<ClientCommand::DriveSub>(subRaw);

	switch (sub)
	{
	case ClientCommand::DriveSub::EnableSequence:
	{
		// Enable sequence 6 -> 7 -> 15 from status; hold CSP target at actual
		// position to avoid a jump on enable. Skip while a fault flag is latched.
		if ((st.nStatusWord & humanoid::cia402::kStatusFaultBit) != 0)
			break;

		cmd.nTargetPosition = st.nActualPosition;

		const std::uint16_t sb = humanoid::cia402::StateBits(st.nStatusWord);
		if (sb == 0x0027U)
			cmd.nControlWord = humanoid::cia402::kControlEnableOperation;
		else if (sb == 0x0023U)
			cmd.nControlWord = humanoid::cia402::kControlEnableOperation;
		else if (sb == 0x0021U)
			cmd.nControlWord = humanoid::cia402::kControlSwitchOn;
		else
			cmd.nControlWord = humanoid::cia402::kControlShutdown;
		break;
	}
	case ClientCommand::DriveSub::QuitStep:
	{
		// Quit step: walk back from EnableOperation toward "not enabled" one
		// CiA 402 transition per cycle.
		const std::uint16_t sb = humanoid::cia402::StateBits(st.nStatusWord);
		if (sb == 0x0027U)
			cmd.nControlWord = humanoid::cia402::kControlSwitchOn;
		else if (sb == 0x0023U)
			cmd.nControlWord = humanoid::cia402::kControlShutdown;
		else if (sb == 0x0021U)
			cmd.nControlWord = humanoid::cia402::kControlShutdown;
		break;
	}
	case ClientCommand::DriveSub::FaultReset:
		// Only assert the fault-reset bit while an error code is latched.
		if (st.nErrorCode == 0)
			break;
		cmd.nControlWord = static_cast<USHORT>(cmd.nControlWord | humanoid::cia402::kControlFaultReset);
		break;
	default:
		break;
	}
}

