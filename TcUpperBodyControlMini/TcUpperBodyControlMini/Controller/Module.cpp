		m_recipForward[i] = false;
		m_recipPosStart[i] = 0;
		m_recipPosTarget[i] = 0;
		m_recipLegStep[i] = 0;
		m_recipLegLength[i] = 0;
		m_recipTf[i] = 0.0;
		m_recipH[i] = 0.0;
		m_recipTa[i] = 0.0;
		m_recipUseTrapezoid[i] = false;

